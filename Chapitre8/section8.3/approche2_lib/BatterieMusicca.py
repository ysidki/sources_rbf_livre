import pyautogui

class BatterieMusicca:

    def cymbale_crash(self):
        pyautogui.press('y')

    def cymbale_ride(self):
        pyautogui.press('u')

    def hi_hat_ferme(self):
        pyautogui.press('r')


    def hi_hat_ouvert(self):
        pyautogui.press('e')

    def hi_hat_pied(self):
        pyautogui.press('c')

    def tom_aigu(self):
        pyautogui.press('g')

    def tom_grave(self):
        pyautogui.press('h')

    def tom_bas(self):
        pyautogui.press('j')

    def caisse_claire(self):
        pyautogui.press('s')

    def caisse_claire_baguettes(self):
        pyautogui.press('d')

    def grosse_caisse(self):
        pyautogui.press('z')

    def hi_hat_ouvert_et_grosse_caisse(self):
        pyautogui.press('z')
        


    def accepter_cookies(self):
        pyautogui.press('tab')
        pyautogui.press('tab')
        pyautogui.press('tab')
        pyautogui.press('enter')
